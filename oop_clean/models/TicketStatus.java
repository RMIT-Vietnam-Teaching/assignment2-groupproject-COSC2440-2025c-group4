package oop_clean.models;

/**
 * Enum representing the status of a ticket.
 * - ACTIVE: Ticket is valid and can be used
 * - USED: Ticket has been used for entry
 * - CANCELLED: Ticket has been cancelled and is no longer valid
 * 
 * @author Group04
 */
public enum TicketStatus {
    ACTIVE,
    USED,
    CANCELLED
}
